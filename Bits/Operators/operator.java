public class operator {
    public static void main(String args[]){
        int n1 = 5, n2 = 6;
        System.out.println(n1+" & "+n2+" = "+(n1&n2));
        System.out.println(n1+" | "+n2+" = "+(n1|n2));
        System.out.println(n1+" ^ "+n2+" = "+(n1^n2));
        System.out.println("~"+n1+" = "+ ~n1);
        System.out.println(n1+" << 2 = "+(n1<<2));
        System.out.println(n1+" >> 2 = "+(n1>>2));
    }
}
